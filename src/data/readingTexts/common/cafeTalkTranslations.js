/**
 * Helper to build translation object for all app languages
 */
export function buildCafeTalkTranslations(wordTranslations) {
  const result = {};

  // For each app language
  Object.keys(wordTranslations).forEach(wordId => {
    const translations = wordTranslations[wordId];

    Object.keys(translations).forEach(lang => {
      if (!result[lang]) result[lang] = {};

      result[lang][wordId] = {
        canonical: translations[lang],
        variants: [translations[lang]]
      };
    });
  });

  return result;
}
