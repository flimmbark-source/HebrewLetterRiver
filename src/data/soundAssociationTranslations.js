/**
 * Creates a translation object for an emoji association
 * Falls back to English if translation not available
 *
 * @param {string} englishWord - The English word key
 * @param {string} appLanguageId - The app language ID (e.g., 'en', 'he', 'es')
 * @returns {object} Object with { word, alt } in the requested language
 */
export function getTranslation(englishWord, appLanguageId = 'en') {
  const translations = emojiTranslations[englishWord];

  if (!translations) {
    console.warn(`No translations found for emoji word: ${englishWord}`);
    return { word: englishWord, alt: englishWord };
  }

  // Return translation for requested language, fallback to English
  return translations[appLanguageId] || translations.en || { word: englishWord, alt: englishWord };
}

/**
 * Helper to create an association object with translations
 *
 * @param {string} emoji - The emoji character
 * @param {string} englishWord - The English word (used as key for translations)
 * @returns {object} Association object with emoji and translations
 */
export function createAssociation(emoji, englishWord) {
  return {
    emoji,
    englishWord, // Store the key for translation lookup
    translations: emojiTranslations[englishWord] || {
      en: { word: englishWord, alt: englishWord }
    }
  };
}

export default {
  emojiTranslations,
  getTranslation,
  createAssociation
};
